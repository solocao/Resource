// 单if语句的代码
public class Demo02If {
	public static void main(String[] args) {
		System.out.println("今天天气不错，在马路上溜达……");
		System.out.println("突然间，发现一个快乐的地方：网吧！");
		int age = 16; // 我的年龄
		
		if (age >= 18) { // 如果成年了，也就是年龄大于等于18
			System.out.println("进入网吧，前台开一个账号。");
			System.out.println("打开电脑，启动LOL，开始high。");
			System.out.println("遇到了一群猪队友，开始骂街。");
			System.out.println("心情不好，前台结账走人。");
		}
		
		System.out.println("回家吃饭饭。");
	}
}